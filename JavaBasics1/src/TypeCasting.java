
public class TypeCasting {

	public static void main(String[] args) {
	
		// Lets you covert types to different types 
		
		int a = (int) 0.5;
		
		System.out.println(a); // Prints out 0 
		
		byte b;
		short c;
		char d;
		int e; 
		long f;
		float g;
		double l = 500.1; 
		
		float ex = a; 

		int exe = (int) l;
		
		System.out.println(exe);
	}

}
