
public class LogicalOperators {

	public static void main(String[] args) {
		
		// || OR 
		// && and 
		// ! not 
		
		boolean a = true || false; 
		
		System.out.println(a);

		boolean b = true && false; 
		
		System.out.println(b);
		
		boolean c = !false;
		
		System.out.println(c);
		
	}

}
