
public class Integers {
	
	public static void main(String[] args) {
		int i = 5;
		int b = 2;
		int c = 0;
		int d = 999999;
		int e = -700;
		
		System.out.println(i);
		System.out.println(i + b * c - d / e);
		
		// Remember that when dividing integers it will not include decimals - then you need to make it a double 
	}

}
